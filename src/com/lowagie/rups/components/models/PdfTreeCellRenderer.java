package com.lowagie.rups.components.models;

import java.awt.Component;

import javax.swing.JTree;
import javax.swing.tree.DefaultTreeCellRenderer;

import com.lowagie.rups.interfaces.TreeNodeWithIcon;

/**
 * Renders the tree cells with the correct icon.
 */
public class PdfTreeCellRenderer extends DefaultTreeCellRenderer {

	/** a serial version UID. */
	private static final long serialVersionUID = 6513462839504342074L;

	/**
	 * @see javax.swing.tree.DefaultTreeCellRenderer#getTreeCellRendererComponent(javax.swing.JTree, java.lang.Object, boolean, boolean, boolean, int, boolean)
	 */
	public Component getTreeCellRendererComponent(JTree tree, Object value,
			boolean selected, boolean expanded, boolean leaf, int row,
			boolean hasFocus) {
		super.getTreeCellRendererComponent(tree, value, selected, expanded, leaf, row, hasFocus);
		if (value instanceof TreeNodeWithIcon) {
			TreeNodeWithIcon node = (TreeNodeWithIcon) value;
			setIcon(node.getIcon());
		}
		return this;
	}
}